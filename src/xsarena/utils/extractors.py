IMPERATIVE_PATTERN = re.compile(
    r"^\s*[-*]\s+(" + "|".join(IMPERATIVE_VERBS) + r")\s+(?P<text>.+)$", re.IGNORECASE
)


def extract_checklists(text: str) -> List[ChecklistItem]:
    """Extract checklist items from markdown text with section info."""
    if not text:
        return []

    lines = text.splitlines()
    current_section = "Introduction"
    items: List[ChecklistItem] = []
    section_line_map = {}  # Map sections to starting line numbers

    for i, ln in enumerate(lines):
        # Track headings to identify sections
        heading_match = _HEADING_PATTERN.match(ln)
        if heading_match:
            current_section = (heading_match.group("title") or "").strip()
            section_line_map[current_section] = i + 1
            continue

        # Check for task list items
        task_match = CHECKLIST_TASK_PATTERN.match(ln)
        if task_match:
            text_content = task_match.group("text").strip()
            items.append(
                ChecklistItem(current_section, text_content, ln.strip(), i + 1)
            )
            continue

        # Check for bullet list items
        bullet_match = CHECKLIST_BULLET_PATTERN.match(ln)
        if bullet_match:
            text_content = bullet_match.group("text").strip()
            # Also check for imperative verbs
            if IMPERATIVE_PATTERN.match(ln):
                items.append(
                    ChecklistItem(current_section, text_content, ln.strip(), i + 1)
                )
            elif "checklist" in current_section.lower():
                # Accept bullets under checklist sections
                items.append(
                    ChecklistItem(current_section, text_content, ln.strip(), i + 1)
                )
            continue

        # Check for numbered list items
        numbered_match = CHECKLIST_NUMBERED_PATTERN.match(ln)
        if numbered_match:
            text_content = numbered_match.group("text").strip()
            items.append(
                ChecklistItem(current_section, text_content, ln.strip(), i + 1)
            )
            continue

    return items


def group_checklist_items_by_section(
    items: List[ChecklistItem],
) -> dict[str, List[ChecklistItem]]:
    """Group checklist items by their section."""
    grouped = {}
    for item in items:
        if item.section not in grouped:
            grouped[item.section] = []
        grouped[item.section].append(item)
    return grouped


def normalize_checklist_items(items: List[ChecklistItem]) -> List[ChecklistItem]:
    """Normalize checklist items by removing duplicates while preserving order."""
    seen = set()
    result = []
    for item in items:
        # Create a key based on section and item text to identify duplicates
        key = (item.section, item.item.lower().strip())
        if key not in seen:
            seen.add(key)
            result.append(item)
    return result


def extract_checklists_from_file(path: str | Path) -> List[ChecklistItem]:
    """Read a markdown file and extract checklist items with section info."""
    p = Path(path)
    if not p.exists():
        return []
    content = p.read_text(encoding="utf-8", errors="replace")
    return extract_checklists(content)
